export function applyTheme(theme: Theme) {
  const r = document.documentElement
  r.style.setProperty('--accent', theme.accent)
  r.style.setProperty('--accent-dim', theme.accentDim)
  r.style.setProperty('--orb1', theme.orb1)
  r.style.setProperty('--orb2', theme.orb2)
  r.style.setProperty('--orb3', theme.orb3)
  r.style.setProperty('--card-bg', theme.cardBg)
  r.style.setProperty('--nav-bg', theme.navBg)
  r.style.setProperty('--base-bg', theme.baseBg)
  r.style.setProperty('--bg-mid', theme.bgMid)
  r.style.setProperty('--bg-deep', theme.bgDeep)
}

export function loadTheme(): Theme {
  const saved = localStorage.getItem('benxp-theme')
  return THEMES.find((t) => t.id === saved) ?? THEMES.find((t) => t.id === 'midnight') ?? THEMES[0]
}

// ── Time-of-day auto theme ─────────────────────────────────────
const TOD_MAP: Array<{ from: number; to: number; id: string }> = [
  { from:  5, to:  8, id: 'sunrise'  },   // dawn
  { from:  8, to: 17, id: ''         },   // day — no override
  { from: 17, to: 20, id: 'dusk'     },   // golden hour
  { from: 20, to: 23, id: 'midnight' },   // evening
  { from: 23, to: 24, id: 'void'     },   // late night
  { from:  0, to:  5, id: 'void'     },   // deep night
]

export function timeThemeEnabled(): boolean {
  return localStorage.getItem('benxp-time-theme') === 'true'
}

export function setTimeThemeEnabled(on: boolean) {
  localStorage.setItem('benxp-time-theme', String(on))
}

/** Returns the theme override ID for the current hour, or null if no override. */
export function getTimeOfDayThemeId(): string | null {
  if (!timeThemeEnabled()) return null
  const h = new Date().getHours()
  for (const slot of TOD_MAP) {
    if (h >= slot.from && h < slot.to) return slot.id || null
  }
  return null
}

/** Applies the time-of-day override (if enabled), falling back to saved user theme. */
export function applyTimeOrSavedTheme() {
  const override = getTimeOfDayThemeId()
  const base     = loadTheme()
  const target   = (override ? THEMES.find(t => t.id === override) : null) ?? base
  applyTheme(target)
  return target
}

export function saveTheme(theme: Theme) {
  localStorage.setItem('benxp-theme', theme.id)
  applyTheme(theme)
  // Auto-switch ambient scene to match theme (lazy import to avoid top-level side effects)
  import('./sounds').then(({ getAmbientForTheme, ambientScene, setAmbientSceneId, ambientEnabled }) => {
    const recommended = getAmbientForTheme(theme.id)
    // Only auto-switch if user hasn't manually pinned a different scene (or always auto-switch)
    if (ambientScene() !== recommended) {
      setAmbientSceneId(recommended, ambientEnabled())
    }
  })
}
